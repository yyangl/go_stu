package dht

import "math/big"

type Route struct {
	buckets []*Bucket
}

var Min = []byte{0x0, 0x0, 0x0, 0x0, 0x0,
	0x0, 0x0, 0x0, 0x0, 0x0,
	0x0, 0x0, 0x0, 0x0, 0x0,
	0x0, 0x0, 0x0, 0x0, 0x0,
}
var Max = []byte{0xf, 0xf, 0xf, 0xf, 0xf,
	0xf, 0xf, 0xf, 0xf, 0xf,
	0xf, 0xf, 0xf, 0xf, 0xf,
	0xf, 0xf, 0xf, 0xf, 0xf,
}

func NewRoute() *Route {
	r := &Route{buckets: make([]*Bucket, 1)}
	min := big.Int{}
	max := big.Int{}

	b := NewBucket(min.SetBytes(Min), max.SetBytes(Max))
	r.buckets = append(r.buckets, b)
	return r
}

